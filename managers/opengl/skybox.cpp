void Skybox::draw() const {
	glActiveTexture(GL_TEXTURE0);
	glBindTexture(GL_TEXTURE_CUBE_MAP, skybox_map.id);
	glBindVertexArray(vao);
	glDrawArrays(GL_TRIANGLES, 0, 36);
}

void Skybox::load_skybox_map(const std::string &path) {
	skybox_map.load_from_asset(path);
}

void Skybox::load_irradiance_map(const std::string &path) {
	irradiance_map.load_from_asset(path);
}